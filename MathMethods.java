public class MathMethods {
	public static void main(String args[]) {

	System.out.println("MATH");
	System.out.println();
	System.out.println("Exponential");
	double pow = Math.pow(2,3);
	System.out.println("double Math.pow(2,3) = " + pow);

	System.out.println();
	System.out.println("Square Root");
	double sqrt = Math.sqrt(16);
	System.out.println("double Math.sqrt(16) = " + sqrt);

	System.out.println();
	System.out.println("Absolute |x|");
	int abs = Math.abs(-5);
	System.out.println("int Math.abs(-5) = " + abs);

	System.out.println();
	System.out.println("Maximal Value");
	int max = Math.max(4, 7);
	System.out.println("int Math.max(4, 7) = " + max);

	System.out.println();
	System.out.println("Minimal Value");
	int min = Math.min(4, 7);
	System.out.println("int Math.min(4, 7) = " + min);	

	}
}
